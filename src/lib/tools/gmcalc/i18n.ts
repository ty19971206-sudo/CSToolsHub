export function fxRequirementLabel(
  lang: Lang,
  pair: string,
  op: FxOp,
  amountCurrency: string
): string {
  const opSym = op === 'divide' ? '÷' : '×';
  if (lang === 'zh') {
    return `请输入 ${pair} 汇率（${amountCurrency} 金额 ${opSym} 汇率 → USD）`;
  }
  return `Enter ${pair} rate (${amountCurrency} amount ${opSym} rate → USD)`;
}

export function tGm(lang: Lang, key: keyof (typeof gmcalcI18n)['zh']): string {
  return gmcalcI18n[lang][key];
}

