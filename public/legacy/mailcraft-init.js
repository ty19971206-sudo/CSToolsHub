            function generateEmail(scene, paramValues, lang) {
                if (scene.id === 'limit_order_not_filled') return buildLimitOrderEmail(paramValues, lang);
                if (scene.id === 'negative_balance_stop_out') return buildNegativeBalanceEmail(paramValues, lang);
                let subject = lang === 'zh' ? scene.subjectZh : scene.subjectEn;
                let body = lang === 'zh' ? scene.bodyZh : scene.bodyEn;
                const regex = /\{\{(\w+)\}\}/g;
                let s = subject, b = body;
                const placeholders = new Set();
                [...s.matchAll(regex), ...b.matchAll(regex)].forEach(m => placeholders.add(m[1]));
                placeholders.forEach(key => {
                    const val = paramValues[key] !== undefined ? paramValues[key] : '';
                    const re = new RegExp(`\\{\\{${key}\\}\\}`, 'g');
                    s = s.replace(re, val);
                    b = b.replace(re, val);
                });
                return { subject: s.trim(), body: b.trim() };
            }

